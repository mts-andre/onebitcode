let primeiro_numero = parseInt(prompt('Digite o primeiro número: '))
let segundo_numero = parseInt(prompt('Digite o segundo número: '))

let resultado_som = primeiro_numero + segundo_numero
let resultado_sub = primeiro_numero - segundo_numero
let resultado_multi = primeiro_numero * segundo_numero
let resultado_div = primeiro_numero / segundo_numero

console.log(`Soma: ${resultado_som}`)
console.log(`Subtração: ${resultado_sub}`)
console.log(`Multiplicação: ${resultado_multi}`)
console.log(`Divisão: ${resultado_div}`)

